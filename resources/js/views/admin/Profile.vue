<template>
    <h1>MI Perfil</h1>
    <h2>{{usuario?.email}}</h2>

</template>

<script setup>
import {ref, onMounted} from "vue";
import * as authService from "../../services/auth.service"

const usuario = ref(null);

onMounted(()=>{
    funPerfil()
})

async function funPerfil(){
    const {data} = await authService.obtenerPerfil()
    usuario.value = data
}
</script>
